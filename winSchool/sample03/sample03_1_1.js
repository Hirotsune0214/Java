let a = 5;
let b = 3;

console.log(`a === b --> ${a === b}`);
console.log(`a !== b --> ${a !== b}`); //true
console.log(`a > b --> ${ a > b }`);
console.log(`a >= b --> ${ a >= b }`);
console.log(`a < b --> ${ a < b }`);
console.log(`a <= b --> ${ a <= b }`);

// 型変換あり比較と厳密比較
console.log(`a == "5" --> ${a == "5"}`);
console.log(`a === "5" --> ${a === "5"}`);